                    // –ü–æ–ª—É—á–µ–Ω–∏–µ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –∏–∑ —Å–ø–∏—Å–∫–∞ —Å–∫–ª–∞–¥—Å–∫–∏—Ö –æ—Å—Ç–∞—Ç–∫–æ–≤
                    var item = items[i];
                    Debug.WriteLine($"   - {item.Product?.Name ?? "No Name"}: {item.QuantityOnHand} —à—Ç.");
                }

                // –í–æ–∑–≤—Ä–∞—Ç —Å–ø–∏—Å–∫–∞ –≤—Å–µ—Ö —Å–∫–ª–∞–¥—Å–∫–∏—Ö –æ—Å—Ç–∞—Ç–∫–æ–≤ —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –ø—Ä–æ–¥—É–∫—Ç–∞—Ö
                return items;
