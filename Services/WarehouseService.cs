        public async Task<List<WarehouseItem>> GetWarehouseItemsAsync()
        {
            try
            {
                // –°–æ–∑–¥–∞–Ω–∏–µ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å–æ —Å–∫–ª–∞–¥–æ–º
                using var context = new AppDbContext();
                Debug.WriteLine("üü° WarehouseService: –ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö —Å–∫–ª–∞–¥–∞...");

                var items = await context.Warehouse // –ó–∞–ø—Ä–æ—Å –∫ —Ç–∞–±–ª–∏—Ü–µ —Å–∫–ª–∞–¥—Å–∫–∏—Ö –æ—Å—Ç–∞—Ç–∫–æ–≤
                    .Include(w => w.Product) // –ó–∞–≥—Ä—É–∂–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø—Ä–æ–¥—É–∫—Ç–∞—Ö
                    .ToListAsync(); // –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∑–∞–ø—Ä–æ—Å–∞ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –≤ —Å–ø–∏—Å–æ–∫

                Debug.WriteLine($"üü¢ WarehouseService: –ó–∞–≥—Ä—É–∂–µ–Ω–æ {items.Count} –∑–∞–ø–∏—Å–µ–π —Å–∫–ª–∞–¥–∞");

                // –õ–æ–≥–∏—Ä—É–µ–º –ø–µ—Ä–≤—ã–µ 3 –∑–∞–ø–∏—Å–∏ –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
                for (int i = 0; i < Math.Min(3, items.Count); i++)
